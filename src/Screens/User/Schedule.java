    private void fitnessTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fitnessTableMouseClicked
        // komanda ispod dohvaca vrijednost koja je zapisana u datom redu i datoj koloni u tabeli (ćelija koju smo selektovali)
        String rowNameSurname = (String) fitnessTable.getValueAt(fitnessTable.getSelectedRow(), fitnessTable.getSelectedColumn());
        // komanda ispod dohvata naziv kolone u kojoj je ćelija koju smo selektovali
        String dayOfTheWeek = (String) fitnessTable.getColumnName(fitnessTable.getSelectedColumn());
        
        // dohvatimo redni broj reda
        int rowIndex = fitnessTable.getSelectedRow();
        String dateTime = fitnessTable.getValueAt(rowIndex, 0).toString();
        
        // komanda ispod vraca niz od dva stringa, gdje je prvi element niza ime, drugi element niza prezime trenera
        String[] name_surname = rowNameSurname.split(" ");
        
        // komanda ispod iz baze dohvaca podatke trenera (tojest, dohvacamo Id trenera)
        int trainerId = GetTrainers.getTrainerId(name_surname[0], name_surname[1]);
        
        // komanda ispod dohvaca rezultat odgovora sa pop-up dijaloga (Yes: 0, No: 1, Cancel: 2)
        int result = JOptionPane.showConfirmDialog(this, "Do you want to book this class?");
        int programId = 1;
        if (result == 0) {
            // Ispod kreiramo novi objekat tipa Reservations i taj objekat proslijedimo metodi setReservation u klasi SetReservations koja vrsi insert u bazu u tabelu "reservations"
            Reservations res = new Reservations();
            res.setProgramId(programId);
            res.setTrainerId(trainerId);
            res.setDateTime(dateTime);
            res.setDayOfTheWeek(dayOfTheWeek);
            res.setUser("TEST");
            
            // poziv metode setReservation koja ce izvrsiti insert u bazu
            SetReservations.setReservation(res);
        }
    }//GEN-LAST:event_fitnessTableMouseClicked

