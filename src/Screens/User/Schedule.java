    private void pilatesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilatesButtonActionPerformed
        // TODO add your handling code here:
        List<Schedules> s = new ArrayList<Schedules>();
        int pilatesProgramId = 5;
        s = GetSchedules.getSchedules(pilatesProgramId);
        int rowCount = pilatesTable.getRowCount();

        for (int i = 0; i < s.size(); i++) {
            String dayOfTheWeek = s.get(i).getDayOfTheWeek();
            String dateTime = s.get(i).getDateTime();
            
            TableColumn tc = pilatesTable.getColumn(dayOfTheWeek);
            int colIndex = tc.getModelIndex();
            
            for (int j = 0; j < rowCount; j++) {
                String row = pilatesTable.getValueAt(j, 0).toString();
                if (row.equals(dateTime)) {
                    pilatesTable.setValueAt(s.get(i).getTrainer().getName() + " " + s.get(i).getTrainer().getSurname(), j, colIndex);
                }
            }
            // Object o = fitnessTable.getValueAt(3, colIndex);
        }
        
        homePanel.setVisible(false);
        fitnessPanel.setVisible(false);
        pilatesPanel.setVisible(true);
        yogaPanel.setVisible(false);
        bodybuildingPanel.setVisible(false);
        crossfitPanel.setVisible(false);
        zumbaPanel.setVisible(false);
    }//GEN-LAST:event_pilatesButtonActionPerformed

