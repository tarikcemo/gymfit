    // fitnessButtonActionPerformed ima za zadatak da sakrije Home panel (i bilo koji drugi panel, ovisno s kojeg panela dolazimo na fitness),
    // te da osim toga dinamicki kreira tabelu sa rasporedom termina za fitness.
    // Tabela se sastoji od vremena termina u razmacima od 2h (od 08:00 do 20:00) predstavljeno kao redovi, te od naziva dana u sedmici predstavljeno kao kolone.
    private void fitnessButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fitnessButtonActionPerformed
        // Prvo kreiramo listu rasporeda. Klasa "Schedules" nam predstavlja objekat koji mapira tabelu Schedules koja se nalazi u bazi (Schedules je predstavljen kao entity).
        
        List<Schedules> s = new ArrayList<Schedules>();
        int fitnessProgramId = 1;
        // Jedan Schedules (ili raspored) dohvatamo iz baze koristeci Id programa za koji nam raspored treba.
        // U slucaju fitnessSchedule koristimo fitnessProgramId = 1.
        // Dati programId proslijedimo funkciji getSchedules() koja se nalazi unutar pomocne klase GetSchedules.
        // Komanda ispod poziva metodu getSchedules() iz GetSchedules klase kojoj proslijedimo programId = 1 i na osnovu kojeg ce nam iz baze dohvatiti sve 
        // redove tabele "schedules" gdje je ProgramId = 1 (dohvaca redove za fitness id).
        s = GetSchedules.getSchedules(fitnessProgramId);
        // Nakon izvrsenja komande iznad, varijabla "s" nam sadrzi potrebne podatke iz baze za fitness program.
        
        // fitnessTable.getRowCount() dohvaca ukupan broj redova u tabeli (mi ih imamo 8)
        int rowCount = fitnessTable.getRowCount();

        // koristeci for petlju ispod prolazimo kroz sve elemente iz liste rasporeda "s"
        for (int i = 0; i < s.size(); i++) {
            // s[0] je prvi element iz liste rasporeda koji predstavlja jedan objekat tipa Schedules, te za taj objekat dohvacamo dan u sedmici u kojem se dati termin nalazi,
            // kao i vrijeme kad dati termin pocinje (naprimjer: 10:00, Wednesday)
            String dayOfTheWeek = s.get(i).getDayOfTheWeek();
            String dateTime = s.get(i).getDateTime();
            
            // Komande ispod dohvacaju specificnu kolonu po njenom nazivu.
            // Vrijednosti koje citamo iz baze za atribut dayOfTheWeek odgovaraju nazivima kolona u tabeli (Wednesday == Wednesday itd).
            // Na osnovu vrijednosti atributa za npr. s[0].dayOfTheWeek (nek to bude Wednesday) mozemo dohvatiti kolonu iz tabele koja ima naziv "Wednesday".
            TableColumn tc = fitnessTable.getColumn(dayOfTheWeek);
            // tc.getModelIndex() dohvaca indeks te kolone, naprimjer za Wednesday je to cetvrta kolona, a index = 3 (posto indeksacija ide od 0).
            int colIndex = tc.getModelIndex();
            
            // for petljom ispod proci cemo kroz sve redove u tabeli (ima ih 8) i po osnovu varijable dateTime koju smo ucitali iz objekta iz baze i
            // kolone koju smo dobili iznad koristeci getColumn() mozemo smjestiti podatak o treneru u odgovarajuci red i odgovarajucu kolonu.
            for (int j = 0; j < rowCount; j++) {
                String row = fitnessTable.getValueAt(j, 0).toString(); // dohvatamo vrijeme u j-tom redu i 0-oj koloni: npr 10:00, 12:00 ili 20:00
                if (row.equals(dateTime)) {
                    // Ako vrijeme iz tabele odgovara vremenu iz baze, u taj red cemo smjestiti podatke iz baze, a u kolonu koja odgovara colIndex koji smo dohvatili
                    // pomocu naziva dana u sedmici (dayOfTheWeek).
                    fitnessTable.setValueAt(s.get(i).getTrainer().getName() + " " + s.get(i).getTrainer().getSurname(), j, colIndex);
                }
            }
        }
        
        homePanel.setVisible(false);
        fitnessPanel.setVisible(true);
        pilatesPanel.setVisible(false);
        yogaPanel.setVisible(false);
        bodybuildingPanel.setVisible(false);
        crossfitPanel.setVisible(false);
        zumbaPanel.setVisible(false);
    }//GEN-LAST:event_fitnessButtonActionPerformed

